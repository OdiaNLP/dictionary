@retry_async(
    retry_on_exceptions=ClientPayloadError,
    max_calls_total=5,
    retry_window_after_first_call_in_seconds=5)
async def fetch(client, page_number):
    delays = [2, 5, 1, 7, 4, 9]
    delay = random.choice(delays)
    print(f"sleeping for {delay} seconds.")
    await asyncio.sleep(delay)
    user_agent = random.choice(user_agents_list)
    base_url = f"https://dsal.uchicago.edu/cgi-bin/app/praharaj_query.py?page={page_number}"
    headers = {
        "User-Agent": user_agent,
        "Pragma": "no-cache",
        "Accept": "*/*",
        "Accept-Encoding": "gzip, deflate, br",
    }
    async with client.get(base_url, headers=headers) as resp:
        return await resp.text()


async def dump_extracted_content(page_number):
    final_text = []
    print(f"Processing page# {page_number}")
    try:
        async with aiohttp.ClientSession() as session:
            response = await fetch(session, page_number)
    except ClientPayloadError as error:
        print(f"Unable to fetch content for page# {page_number} due to: {error}")
        response = None
    print(f"Got response for page# {page_number}")
    final_text.append(response)
    return final_text


def write_extracted_content(final_text, filename):
    with open(filename, 'wb') as fh:
        pickle.dump(final_text, fh)
    print(f"Written to the file: {filename}")


async def main():
    final_text = []
    for chunk in range(1, 9249, 100):
        try:
            print(f"retrieving for {chunk} to {chunk+100} size.")
            temp_text = await asyncio.gather(*(dump_extracted_content(page_number)
                                               for page_number in tqdm(range(chunk, chunk + 100))))
            write_extracted_content(final_text, f"intermediate_dump_{chunk+100}.txt")
            print("Block sleeping for 3 minutes")
            sleep(180)
            final_text.extend(temp_text)
        except Exception as error:
            print(f"Unable to chunk for the range: {chunk} to {chunk+100} due to error: {error}")
            print("sleeping for 5 minutes")
            sleep(300)
        write_extracted_content(final_text, "full_dump.txt")


if __name__ == '__main__':
    asyncio.run(main())
